// Note: This handler implementation is a template. The actual handler methods
// are generated by go-swagger from the OpenAPI spec defined in api/v2.0/swagger.yaml.
//
// To complete the implementation:
// 1. Update api/v2.0/swagger.yaml with PAT endpoints (DONE)
// 2. Run: cd src && make generate_apis
// 3. This will generate the operation handlers in src/server/v2.0/restapi/operations/user/
// 4. Implement the handler methods in a struct that embeds BaseAPI
//
// Below is a reference implementation showing the business logic needed.

type patAPI struct {
	BaseAPI
	ctl pat_ctl.Controller
}

func newPatAPI() *patAPI {
	return &patAPI{
		ctl: pat_ctl.Ctl,
	}
}

// toPatModel converts an internal PAT model to the API response format
func toPatModel(pat *model.PersonalAccessToken) map[string]interface{} {
	result := map[string]interface{}{
		"id":             pat.ID,
		"user_id":        pat.UserID,
		"name":           pat.Name,
		"description":    pat.Description,
		"disabled":       pat.Disabled,
		"is_legacy":      pat.IsLegacy,
		"creation_time":  pat.CreationTime,
		"update_time":    pat.UpdateTime,
		"expires_at":     pat.ExpiresAt,
	}

	// Set expired status
	if pat.ExpiresAt > 0 {
		result["expired"] = pat.ExpiresAt <= time.Now().Unix()
	} else {
		result["expired"] = false
	}

	// Set last_used_at if available
	if pat.LastUsedAt > 0 {
		result["last_used_at"] = pat.LastUsedAt
	}

	return result
}
